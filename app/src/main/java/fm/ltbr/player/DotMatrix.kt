        '*' to intArrayOf(0x00, 0x0a, 0x04, 0x1f, 0x04, 0x0a, 0x00),
        '#' to intArrayOf(0x0a, 0x0a, 0x1f, 0x0a, 0x1f, 0x0a, 0x0a),
        '%' to intArrayOf(0x19, 0x1a, 0x02, 0x04, 0x08, 0x0b, 0x13),
        '@' to intArrayOf(0x0e, 0x11, 0x17, 0x15, 0x17, 0x10, 0x0e),
    )

    // Typographic characters that ICY titles often carry, mapped to plain glyphs.
    private val ALIASES: Map<Char, Char> = mapOf(
        '’' to '\'', '‘' to '\'', '“' to '"', '”' to '"',
        '–' to '-', '—' to '-', '•' to '·', '…' to '.',
    )

    class Matrix(val width: Int, private val bits: ByteArray) {
        fun on(x: Int, y: Int): Boolean = bits[y * width + x].toInt() != 0
    }

    fun rasterise(text: String): Matrix {
        val padded = "   $text   "
        // Uppercase + strip diacritics so "Édith" renders as "EDITH".
        val plain = Normalizer.normalize(padded.uppercase(), Normalizer.Form.NFD)
            .replace(Regex("\\p{Mn}+"), "")
        val chars = plain.map { ALIASES[it] ?: it }

        val width = chars.size * GLYPH_STEP
        val bits = ByteArray(width * ROWS)
        chars.forEachIndexed { i, ch ->
            val glyph = FONT[ch] ?: FONT.getValue(' ')
            for (y in 0 until ROWS) {
                val rowBits = glyph[y]
                for (x in 0 until GLYPH_W) {
                    if (rowBits and (1 shl (GLYPH_W - 1 - x)) != 0) {
                        bits[y * width + i * GLYPH_STEP + x] = 1
                    }
                }
            }
        }
        return Matrix(width, bits)
    }
}

private val DotOn = Color(0xFFFF9B21)
private val DotOff = Color(0xFF241705)

/**
 * The scrolling display. Dots march right-to-left while [scrolling]; when idle
 * the text sits still with the ghost dots visible, like a real head unit.
 */
@Composable
fun DotMatrixDisplay(
    text: String,
    scrolling: Boolean,
    modifier: Modifier = Modifier,
) {
    val matrix = remember(text) { MatrixFont.rasterise(text) }
    var scrollX by remember { mutableFloatStateOf(0f) }

    LaunchedEffect(text) { scrollX = 0f }
    LaunchedEffect(scrolling) {
        if (scrolling) {
            var last = 0L
            while (true) {
                withFrameNanos { now ->
                    if (last != 0L) {
                        scrollX += (now - last) / 1_000_000f * 0.022f
                    }
                    last = now
                }
            }
        }
    }

    Canvas(modifier = modifier) {
        val dot = 3.dp.toPx()
        val gap = 1.dp.toPx()
        val pitch = dot + gap
        val cols = (size.width / pitch).toInt()
        val gridH = MatrixFont.ROWS * pitch - gap
        val yTop = (size.height - gridH) / 2f
        val base = scrollX.toInt()

        for (cx in 0 until cols) {
            val mx = ((base + cx) % matrix.width + matrix.width) % matrix.width
            for (y in 0 until MatrixFont.ROWS) {
                drawRect(
                    color = if (matrix.on(mx, y)) DotOn else DotOff,
                    topLeft = Offset(cx * pitch, yTop + y * pitch),
                    size = Size(dot, dot),
                )
            }
        }
    }
}
